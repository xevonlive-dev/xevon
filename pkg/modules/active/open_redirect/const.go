var topParamRegexes = []*regexp.Regexp{
	regexp.MustCompile("(?i)(.*url.*)"),
	regexp.MustCompile("(?i)(.*uri.*)"),
	regexp.MustCompile("(?i)(.*link.*)"),
	regexp.MustCompile("(?i)(.*location.*)"),
	regexp.MustCompile("(?i)(.*next.*)"),
	regexp.MustCompile("(?i)(.*redirect.*)"),
	regexp.MustCompile("(?i)(.*back.*)"),
	regexp.MustCompile("(?i)(.*return.*)"),
	regexp.MustCompile("(?i)(.*cb.*)"),
	regexp.MustCompile("(?i)(.*callback.*)"),
}

func matchTopParams(str string) bool {
	for _, param := range topParams {
		if strings.Contains(str, param) {
			return true
		}
	}
	for _, param := range topParamRegexes {
		if param.MatchString(str) {
			return true
		}
	}
	return false
}

var valueIsURLRegex = regexp.MustCompile(`(?i)(https?(://|%3A%2F%2F))`)

func matchValueIsURL(str string) bool {
	return valueIsURLRegex.MatchString(str)
}
