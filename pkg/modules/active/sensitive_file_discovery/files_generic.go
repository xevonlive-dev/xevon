// markerBasedPaths returns the set of paths from marker-based sensitiveFiles
// for deduplication checking.
func markerBasedPaths() map[string]bool {
	m := make(map[string]bool, len(sensitiveFiles))
	for _, sf := range sensitiveFiles {
		m[sf.path] = true
	}
	return m
}
